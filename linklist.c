#include "shell.h"

list_t *add_node_end(list_t **head, char *dir);
void free_list(list_t *head);
void free_alias_list(alias_t *head);

/**
 * free_alias_list - free a alias_t list
 * @head: head of list
 */
void free_alias_list(alias_t *head)
{
	alias_t *next;

	while (head)
	{
		next = head->next;
		free(head->name);
		free(head->value);
		free(head);
		head = next;
	}
}

/**
 * add_node_end - adds node
 * @head: pointer
 * @dir: directory
 * Return: Null
 */
list_t *add_node_end(list_t **head, char *dir)
{
	list_t *new_node = malloc(sizeof(list_t));
	list_t *last;

	if (!new_node)
		return (NULL);

	new_node->dir = dir;
	new_node->next = NULL;

	if (!head)
	{
		last = *head;
		while (last->next != NULL)
			last = last->next;
		last->next = new_node;
	}
	else
		*head = new_node;

	return (new_node);
}
/**
 * free_list - free list
 * @head: head list
 */
void free_list(list_t *head)
{
	list_t *next;

	while (head)
	{
		next = head->next;
		free(head->dir);
		free(head);
		head = next;
	}
}
